<template>
  <div class="hello">
    <h1>{{ msg }}</h1>
    <h2>Essential Links</h2>
    <Counter v-bind:num="num" v-on:incre="increment" v-on:decre="decrement"/>
    <p>parent:{{num}}</p>
  </div>
</template>

<script>
import Counter from './Counter'
export default {
  name: 'HelloWorld',
  data () {
    return {
      num: 5,
      msg: 'Welcome to Your Vue.js App'
    }
  },
  components: {
    Counter
  },
  methods: {
    increment() {
      this.num++;
    },
    decrement() {
      this.num--;
    }
  }
}
</script>

